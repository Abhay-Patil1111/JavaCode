package basiccode;

public class Caseconversion {
    public static void main(String[] args) {
        String str1 = "Abhay Parth";

        String lowercaseStr = str1.toLowerCase();
        System.out.println("String is lowercse:" + lowercaseStr);

        String uppercaseStr = str1.toUpperCase();
        System.out.println("String is Uppercase:" + uppercaseStr);
    }
}